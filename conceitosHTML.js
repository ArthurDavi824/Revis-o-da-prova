


//<p></p> - Tag utilizada para inserir textos como parágrafos.

//<h1> a <h6> - Os títulos em HTML vão de h1 até h6. Sendo h1 o título de maior importância na página, e h6 o de menor importância.

//<strong></strong> - A tag define um texto importante ou enfatizado, geralmente colocado em negrito

//<span></span> - agrupar elementos para fins de estilo (usando os atributos class ou id ), ou para compartilhar valores de atributos

//<label></label> -  traduzida quer dizer rótulo, é utilizada para associar um rótulo a um elemento de controle de um formulário, inserindo uma descrição textual para um campo específico, facilitando a compreensão do usuário.

//<b></b> - deixa o seu texto em negrito.

//<i></i> - deixa o seu texto em itálico.

//<br> - quebra de linha.

//<title></title> - define o titulo do documento

//--------------------------------------------------


//<div></div> - “CAIXAS” as <div> são muito utilizadas e desempenham um papel importante na organização das páginas Web.

//<header></header> - Essa tag define um cabeçalho, ou seja, a parte do topo, onde inicia-se a página.

//<main></main> -Essa tag representa o conteúdo principal do seu corpo, ou seja, o conteúdo principal da sua página. 

//<footer></footer> - Essas tags definem um rodapé para a página, geralmente utilizadas no final da página;

//<section></section> - Essa tag define uma sessão para sua página. Utilizada para separar e organizar os conteúdos.

//<article></article> - Essa tag define um artigo da sua página. Ela é utilizada para separar o conteúdo da sua página. É mais utilizada para criação de blogs, um artigo de revista ou jornal, página de conteúdos e etc.

//<aside></aside> - A tag <aside> é utilizada para apresentar um conteúdo que está relacionado ao conteúdo principal da página, e que pode ser considerado como um “extra” ou um conteúdo secundário

//<nav></nav> - Essa tag define um conteúdo de navegação. Por isso, é muito utilizado em conjunto com listas e na criação de menus. Ou seja, uma seção com links de navegação.


//--------------------------------------------------

//<form></form> - Formulários HTML são elementos fundamentais na interatividades das páginas. Com eles é possível que os usuários forneçam dados que podem ser enviados para os servidores para processamento

//<Input /> - Os inputs são os locais onde é possível entrar diversos tipos de dados. Eles de diferenciam por meio do atributo type.

//<button></button> - Os botões são elementos cruciais na interatividade dentro de uma página web. 

//<a></a> - A tag <a> (âncora) no HTML tem o seguinte formato:
//<a href="https://www.google.com">Clique aqui e vá para o Google</a>

//<img> - A tag <img> é usada para incorporar imagens dentro de uma página Web.

//<video></video> - A tag <video> é utilizada para incorporar conteúdo em vídeo em uma página web.